public enum IssueSeverity {
    HIGH(3), MEDIUM(2), LOW(1);

    private int numVal;

    IssueSeverity(int numVal) {
        this.numVal = numVal;
    }

    public int getSeverityLevel() {
        return numVal;
    }
}
